const SPRITE_BASE = 'https://play.pokemonshowdown.com/sprites';
const POKEAPI_BASE = 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon';

/**
 * Showdown sometimes stores species with the form name first
 * (e.g. "Wash Rotom" instead of "Rotom-Wash", "Heat Rotom" instead of "Rotom-Heat").
 * This map corrects reversed display names to their canonical sprite IDs.
 */
const REVERSED_FORM_MAP: Record<string, string> = {
  'wash rotom': 'rotom-wash',
  'heat rotom': 'rotom-heat',
  'frost rotom': 'rotom-frost',
  'fan rotom': 'rotom-fan',
  'mow rotom': 'rotom-mow',
  'charizard y-mega': 'charizard-megay',
  'charizard x-mega': 'charizard-megax',
};

/**
 * Convert a species display name (from team JSONB) to a Showdown sprite ID.
 * Handles reversed Showdown display names (e.g. "Wash Rotom" → "rotom-wash"),
 * Mega suffixes, and regional forms.
 */
export function speciesToSpriteId(species: string): string {
  const lower = species.toLowerCase();
  // Check reversed form map first
  if (REVERSED_FORM_MAP[lower]) return REVERSED_FORM_MAP[lower];
  // Standard: strip non-alphanumeric except hyphens
  return lower.replace(/[^a-z0-9-]/g, '');
}

/**
 * Get the primary sprite URL (Showdown home sprite).
 * The spriteId is already in Showdown's hyphenated format (set during seed).
 * e.g., "raichu-alola", "charizard-megax", "garchomp"
 */
export function getShowdownSprite(spriteId: string): string {
  return `${SPRITE_BASE}/home/${spriteId}.png`;
}

/**
 * Get PokeAPI sprite by dex number (reliable for base forms).
 */
export function getPokeApiSprite(dexNum: number): string {
  return `${POKEAPI_BASE}/other/home/${dexNum}.png`;
}

/**
 * Get the basic PokeAPI sprite (lower quality but highest coverage).
 */
export function getPokeApiBasicSprite(dexNum: number): string {
  return `${POKEAPI_BASE}/${dexNum}.png`;
}

