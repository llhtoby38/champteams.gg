/**
 * Get the Serebii Legends Z-A sprite URL for a Mega Pokemon, if available.
 * Returns null if the Mega form is not covered (caller should fall through
 * to other sprite sources).
 */
export function getSerebiiMegaSprite(spriteId: string): string | null {
  const dex = SEREBII_MEGA_DEX[spriteId];
  if (!dex) return null;
  let variant = 'm';
  if (spriteId.endsWith('-megax')) variant = 'mx';
  else if (spriteId.endsWith('-megay')) variant = 'my';
  else if (spriteId.endsWith('-megaz')) variant = 'mz';
  return `https://www.serebii.net/legendsz-a/pokemon/${dex}-${variant}.png`;
}

